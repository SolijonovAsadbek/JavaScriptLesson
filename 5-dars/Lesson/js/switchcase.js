// switchcase

let day = 'Monday'

switch (day){
    case 'Monday':
        console.log('Monday-Dushanba');
        break;
    case 'Tuesday':
        console.log('Tuesday-Seshanba');
        break;
    case 'Wednethday':
        console.log('wednethday - Chorshanba');
        break;
    case 'Therthday':
        console.log('Thertyday - Payshanba');
        break;
    default:
        console.log('Hafta kunlari yo`q');

}
